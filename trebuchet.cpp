int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    // freopen(".in","r",stdin);
    // freopen(".out","w",stdout);
    long long sum=0;
    for(int i=0;i<1000;i++){
        string in;
        cin >> in;
        cout << in << "\n";
        int lastnum=-1;
        for(int j=0;j<in.length();j++){
            if(in[j]<='9'){
                if( lastnum ==-1){
                    sum+=((in[j]-'0')*10);
                }
                lastnum=in[j]-'0';
            }
        }
        sum+=lastnum;
    }
    cout << sum << "\n";
    
}
